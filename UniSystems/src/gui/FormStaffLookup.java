    private void btnEdtDltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdtDltActionPerformed
        // TODO add your handling code here:
        Boolean found = false;
        for (Staff people : staff.getStaffMembers()) {
            if (people.getStaffRefNumb().equals(this.txtStaffNumb.getText())) {
                FormStaffDetails frm = new FormStaffDetails(people.getStaffRefNumb());
                frm.setVisible(true);
                found = true;
                break;
            }
        }
        if (!found){
            JOptionPane.showMessageDialog(null,
                "The Staff Number entered was not found.",
                "Staff Lookup Error",
                JOptionPane.WARNING_MESSAGE);//10438524
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnEdtDltActionPerformed

